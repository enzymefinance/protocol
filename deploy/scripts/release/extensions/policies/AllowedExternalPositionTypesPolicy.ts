import { AllowedExternalPositionTypesPolicyArgs } from '@enzymefinance/protocol';
import { DeployFunction } from 'hardhat-deploy/types';

const fn: DeployFunction = async function (hre) {
  const {
    deployments: { deploy, get },
    ethers: { getSigners },
  } = hre;

  const deployer = (await getSigners())[0];
  const policyManager = await get('PolicyManager');

  await deploy('AllowedExternalPositionTypesPolicy', {
    args: [policyManager.address] as AllowedExternalPositionTypesPolicyArgs,
    from: deployer.address,
    linkedData: {
      type: 'POLICY',
    },
    log: true,
    skipIfAlreadyDeployed: true,
  });
};

fn.tags = ['Release', 'Policies', 'AllowedExternalPositionTypesPolicy'];
fn.dependencies = ['PolicyManager'];

export default fn;
